package navigation;

public class cameraComponents {

	private float xComponent;
	private float yComponent;
	private float zComponent;
	private float rotation;
	
	public cameraComponents(float xComponent, float yComponent, float zComponent, float rotation) {
		
		this.xComponent = xComponent;
		this.yComponent = yComponent;
		this.zComponent = zComponent;
		this.rotation = rotation;
	}
	
	
}
